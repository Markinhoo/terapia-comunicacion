create or replace function public.registrar_invitacion_usuario(
  p_email text,
  p_role text
)
returns bigint
language plpgsql
security definer
set search_path = public, auth
as $function$
declare
  v_id bigint;
begin
  if not public.can_manage_users() then
    raise exception using errcode = '42501', message = 'Solo el administrador maestro puede registrar usuarios.';
  end if;

  if coalesce(trim(p_email), '') !~* '^[^@\s]+@[^@\s]+\.[^@\s]+$' then
    raise exception using errcode = '22023', message = 'El correo no es valido.';
  end if;

  if p_role not in ('master_admin', 'admin', 'user') then
    raise exception using errcode = '22023', message = 'El rol no es valido.';
  end if;

  insert into public.invitaciones_usuario (email, role, created_by, estado)
  values (lower(trim(p_email)), p_role, auth.uid(), 'pendiente')
  returning id into v_id;

  return v_id;
end;
$function$;

create or replace function public.actualizar_rol_usuario(
  p_user_id uuid,
  p_role text,
  p_active boolean
)
returns void
language plpgsql
security definer
set search_path = public, auth
as $function$
begin
  if not public.can_manage_users() then
    raise exception using errcode = '42501', message = 'Solo el administrador maestro puede cambiar roles.';
  end if;

  if p_role not in ('master_admin', 'admin', 'user') then
    raise exception using errcode = '22023', message = 'El rol no es valido.';
  end if;

  update public.app_profiles
  set role = p_role,
      active = p_active,
      updated_at = now()
  where id = p_user_id;
end;
$function$;

