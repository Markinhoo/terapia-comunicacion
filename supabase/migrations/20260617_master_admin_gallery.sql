update public.app_profiles
set role = 'master_admin'
where id = (
  select id
  from public.app_profiles
  where role = 'admin'
  order by created_at
  limit 1
)
and not exists (
  select 1 from public.app_profiles where role = 'master_admin'
);

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public, auth
as $function$
  select
    not exists (
      select 1
      from public.app_profiles
      where role in ('master_admin', 'admin')
        and active
    )
    or exists (
      select 1
      from public.app_profiles
      where id = auth.uid()
        and role in ('master_admin', 'admin')
        and active
    );
$function$;

