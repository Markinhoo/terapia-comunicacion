create or replace function public.eliminar_paciente_admin(p_paciente_id bigint)
returns void
language plpgsql
security definer
set search_path = pg_catalog, public
as $function$
declare
  v_archivado integer;
begin
  if not public.is_admin() then
    raise exception using errcode = '42501', message = 'Solo un administrador puede archivar pacientes.';
  end if;

  update public.pacientes
  set activo = false
  where id = p_paciente_id;

  get diagnostics v_archivado = row_count;

  update public.citas
  set estatus = 'Cancelada',
      confirmado = false
  where paciente_id = p_paciente_id
    and estatus is distinct from 'Cancelada';

  if v_archivado = 0 then
    raise exception using errcode = 'P0002', message = 'El paciente no existe.';
  end if;
end;
$function$;

