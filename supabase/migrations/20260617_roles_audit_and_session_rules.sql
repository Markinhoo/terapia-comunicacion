create or replace function public.validar_sesion_paciente()
returns trigger
language plpgsql
set search_path = public
as $function$
begin
  if exists (
    select 1
    from public.sesiones_paciente
    where paciente_id = new.paciente_id
      and fecha_terapia = new.fecha_terapia
      and id is distinct from new.id
  ) then
    raise exception using
      errcode = '23505',
      message = 'Ya existe un registro para esa fecha de terapia.';
  end if;

  return new;
end;
$function$;

