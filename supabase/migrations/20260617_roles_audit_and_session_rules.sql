before insert or update on public.sesiones_paciente
for each row execute function public.validar_sesion_paciente();

create table if not exists public.app_profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null,
  role text not null default 'user' check (role in ('admin', 'user')),
  active boolean not null default true,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now()
);

create table if not exists public.invitaciones_usuario (
  id bigint generated by default as identity primary key,
  email text not null,
  role text not null default 'user' check (role in ('admin', 'user')),
  estado text not null default 'pendiente' check (estado in ('pendiente', 'enviada', 'aceptada', 'cancelada')),
  created_by uuid references auth.users(id),
  created_at timestamp with time zone not null default now()
);

create table if not exists public.bitacora_cambios (
  id bigint generated by default as identity primary key,
  tabla text not null,
  registro_id text,
  accion text not null,
  usuario_id uuid references auth.users(id),
  usuario_email text,
  cambios jsonb,
  created_at timestamp with time zone not null default now()
);

