create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public, auth
as $function$
begin
  insert into public.app_profiles (id, email, role)
  values (new.id, coalesce(new.email, ''), 'user')
  on conflict (id) do nothing;

  return new;
end;
$function$;

drop trigger if exists on_auth_user_created_app_profile on auth.users;
create trigger on_auth_user_created_app_profile
after insert on auth.users
for each row execute function public.handle_new_auth_user();

insert into public.app_profiles (id, email, role)
select
  u.id,
  coalesce(u.email, ''),
  case
    when not exists (
      select 1 from public.app_profiles p where p.role = 'admin'
    ) then 'admin'
    else 'user'
  end
from auth.users u
on conflict (id) do nothing;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public, auth
as $function$
  select
    not exists (select 1 from public.app_profiles where role = 'admin' and active)
    or exists (
      select 1
      from public.app_profiles
      where id = auth.uid()
        and role = 'admin'
        and active
    );
$function$;

create or replace function public.can_write()
returns boolean
language sql
stable
security definer
set search_path = public, auth
as $function$
  select public.is_admin();
$function$;

create or replace function public.registrar_invitacion_usuario(
  p_email text,
  p_role text
)
returns bigint
language plpgsql
security definer
set search_path = public, auth
as $function$
declare
  v_id bigint;
begin
  if not public.is_admin() then
    raise exception using errcode = '42501', message = 'Solo un administrador puede registrar usuarios.';
  end if;

  if coalesce(trim(p_email), '') !~* '^[^@\s]+@[^@\s]+\.[^@\s]+$' then
    raise exception using errcode = '22023', message = 'El correo no es valido.';
  end if;

  if p_role not in ('admin', 'user') then
    raise exception using errcode = '22023', message = 'El rol no es valido.';
  end if;

  insert into public.invitaciones_usuario (email, role, created_by)
  values (lower(trim(p_email)), p_role, auth.uid())
  returning id into v_id;

  return v_id;
end;
$function$;

create or replace function public.actualizar_rol_usuario(
  p_user_id uuid,
  p_role text,
  p_active boolean
)
returns void
language plpgsql
security definer
set search_path = public, auth
as $function$
begin
  if not public.is_admin() then
    raise exception using errcode = '42501', message = 'Solo un administrador puede cambiar roles.';
  end if;

  if p_role not in ('admin', 'user') then
    raise exception using errcode = '22023', message = 'El rol no es valido.';
  end if;

  update public.app_profiles
  set role = p_role,
      active = p_active,
      updated_at = now()
  where id = p_user_id;
end;
$function$;

create or replace function public.audit_table_change()
returns trigger
language plpgsql
security definer
set search_path = public, auth
as $function$
declare
  v_email text;
  v_record_id text;
  v_changes jsonb;
begin
  select email into v_email
  from auth.users
  where id = auth.uid();

  if tg_op = 'DELETE' then
    v_record_id := coalesce(to_jsonb(old)->>'id', null);
    v_changes := jsonb_build_object('old', to_jsonb(old));
  elsif tg_op = 'UPDATE' then
    v_record_id := coalesce(to_jsonb(new)->>'id', to_jsonb(old)->>'id');
    v_changes := jsonb_build_object('old', to_jsonb(old), 'new', to_jsonb(new));
  else
    v_record_id := coalesce(to_jsonb(new)->>'id', null);
    v_changes := jsonb_build_object('new', to_jsonb(new));
  end if;

  insert into public.bitacora_cambios (
    tabla,
    registro_id,
    accion,
    usuario_id,
    usuario_email,
    cambios
  )
  values (
    tg_table_name,
    v_record_id,
    tg_op,
    auth.uid(),
    v_email,
    v_changes
  );

  return coalesce(new, old);
end;
$function$;

do $$
declare
  v_table text;
begin
  foreach v_table in array array[
    'pacientes',
    'citas',
    'paciente_detalle',
    'expedientes',
    'paciente_objetivos',
    'objetivos_terapeuticos',
    'archivos_paciente',
    'sesiones_paciente'
  ]
  loop
    execute format('drop trigger if exists audit_%I on public.%I', v_table, v_table);
    execute format(
      'create trigger audit_%I after insert or update or delete on public.%I for each row execute function public.audit_table_change()',
      v_table,
      v_table
    );
  end loop;
end $$;

