do $$
declare
  v_table text;
  v_policy record;
begin
  foreach v_table in array array[
    'pacientes',
    'citas',
    'paciente_detalle',
    'expedientes',
    'paciente_objetivos',
    'objetivos_terapeuticos',
    'archivos_paciente',
    'sesiones_paciente',
    'envios_reglamento'
  ]
  loop
    if to_regclass(format('public.%I', v_table)) is not null then
      execute format('alter table public.%I enable row level security', v_table);

      for v_policy in
        select policyname
        from pg_policies
        where schemaname = 'public'
          and tablename = v_table
      loop
        execute format(
          'drop policy if exists %I on public.%I',
          v_policy.policyname,
          v_table
        );
      end loop;

      execute format(
        'create policy %I on public.%I for select to authenticated using (true)',
        v_table || '_lectura_autenticada',
        v_table
      );

      execute format(
        'create policy %I on public.%I for insert to authenticated with check (public.can_write())',
        v_table || '_insert_admin',
        v_table
      );

      execute format(
        'create policy %I on public.%I for update to authenticated using (public.can_write()) with check (public.can_write())',
        v_table || '_update_admin',
        v_table
      );

      execute format(
        'create policy %I on public.%I for delete to authenticated using (public.can_write())',
        v_table || '_delete_admin',
        v_table
      );
    end if;
  end loop;
end $$;

create or replace function public.reactivar_paciente_con_cita()
returns trigger
language plpgsql
security definer
set search_path = public
as $function$
begin
  if new.paciente_id is not null then
    update public.pacientes
    set activo = true
    where id = new.paciente_id;
  end if;

  return new;
end;
$function$;

